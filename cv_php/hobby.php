        <p>La peinture abstraite est une forme d’art contemporain. Non figuratif, cet art utilise des codes 
            différents dans sa réalisation ainsi que les sens et l’imagination de ceux qui la regardent.
            Le but n’est pas de copier la réalité, ni d’en créer une, mais de créer une expérience visuelle
            et de ressentir des émotions.</p>
        <br>
        <p>J'ai adoré créer des peintures abstraites pour ma maison, elles symbolisent une émotion que je ne
            verbalise pas forcément, ou changent complétement l'esthetique d'une pièce.
            Il n'y a pas besoin d'avoir une technique particulière, ni beaucoup de materiel, on peut prendre 
            des échantillons de peinture pour les murs, peindre aves ses doigts, ajouter des jeux de textures 
            avec par exemple du papier cellophane, etc... La créativitée n'a aucune limite !
        </p>
